                exeOne = Helping.getExecutionTime(startingTimeOne, finishingTimeOne);
                jTextField2.setText(exeOne + " sec");

                int i = 1;
                for (String lnk : Links.validLinks) {
                    model1.insertRow(model1.getRowCount(), new Object[]{Integer.toString(i++),lnk});
                }
                i = 1;
                for (String lnk : Links.invalidLinks) {
                    model2.insertRow(model2.getRowCount(), new Object[]{Integer.toString(i++),lnk});
                }
                Links.invalidLinks.clear();
                Links.validLinks.clear();

                //.............................................................................................

                
                DateFormat s2DateFormat = new SimpleDateFormat("HH:mm:ss");
                Date s2Date = new Date();
                String startingTimeTwo = s2DateFormat.format(s2Date);

                TwoThreads.main(s);

                DateFormat e2DateFormat = new SimpleDateFormat("HH:mm:ss");
                Date e2Date = new Date();
                String finishingTimeTwo = e2DateFormat.format(e2Date);

                int exeTwo;
                exeTwo = Helping.getExecutionTime(startingTimeTwo, finishingTimeTwo);
